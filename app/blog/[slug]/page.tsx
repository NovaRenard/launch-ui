export default async function BlogPostPage({
  params,
}: {
  params: Promise<{ slug: string }>;
}) {
  const { slug } = await params;
  const post = posts[slug];

  if (!post) {
    notFound();
  }

  const lines = post.content.split("\n");

  return (
    <main className="bg-background text-foreground min-h-screen w-full">
      <Navbar />
      <article className="mx-auto max-w-2xl px-4 py-16">
        <div className="mb-8">
          <Link
            href="/blog"
            className="text-muted-foreground hover:text-foreground inline-flex items-center gap-1.5 text-sm transition-colors"
          >
            ← Назад в блог
          </Link>
        </div>

        <header className="mb-12">
          <div className="mb-4 flex items-center gap-3">
            <span className="text-brand-foreground text-xs font-semibold uppercase tracking-wider">
              {post.category}
            </span>
            <span className="text-muted-foreground text-xs">·</span>
            <span className="text-muted-foreground text-xs">{post.readTime} чтения</span>
          </div>
          <h1 className="text-3xl font-bold leading-tight sm:text-4xl mb-4">
            {post.title}
          </h1>
          <p className="text-muted-foreground text-sm">{post.date}</p>
        </header>

        <div className="prose-custom space-y-4 text-[15px] leading-relaxed">
          {lines.map((line, i) => {
            if (line.startsWith("## ")) {
              return (
                <h2 key={i} className="text-xl font-bold mt-10 mb-3">
                  {line.slice(3)}
                </h2>
              );
            }
            if (line.startsWith("- ")) {
              return (
                <li key={i} className="text-muted-foreground ml-4 list-disc">
                  {line.slice(2)}
                </li>
              );
            }
            if (line.match(/^\d+\. /)) {
              return (
                <li key={i} className="text-muted-foreground ml-4 list-decimal">
                  {line.replace(/^\d+\. /, "").replace(/\*\*(.*?)\*\*/g, "$1")}
                </li>
              );
            }
            if (line.startsWith("```")) {
              return null;
            }
            if (line.trim() === "") {
              return <div key={i} className="h-2" />;
            }
            return (
              <p key={i} className="text-muted-foreground">
                {line}
              </p>
            );
          })}
        </div>

        <div className="mt-16 border-t border-border pt-10">
          <p className="text-muted-foreground text-sm mb-4">
            Хотите внедрить AI-агента в своём бизнесе?
          </p>
          <a
            href="https://t.me/FlowAi_Manager"
            target="_blank"
            rel="noopener noreferrer"
            className="inline-flex items-center gap-2 rounded-xl bg-foreground text-background px-5 py-2.5 text-sm font-semibold hover:opacity-90 transition-opacity"
          >
            Написать в Telegram
          </a>
        </div>
      </article>
      <Footer />
    </main>
  );
}
